console.log("La resta de: " + numero1 + " - " + numero2 + " = " + resta);
console.log("La multiplicacion de: " + numero1 + " * " + numero2 + " = " + multiplicacion);
console.log("La division de: " + numero1 + " / " + numero2 + " = " + division);
